do $$
declare
  table_name text;
begin
  foreach table_name in array array[
    'numeris_modules',
    'numeris_signals',
    'numeris_simulations',
    'numeris_reports',
    'numeris_event_stream',
    'numeris_brain_traits',
    'numeris_brain_regions',
    'numeris_entrance_sequences',
    'numeris_geopolitical_news'
  ]
  loop
    execute format('drop policy if exists "%s_public_read" on public.%I', table_name, table_name);
    execute format('create policy "%s_public_read" on public.%I for select using (true)', table_name, table_name);
  end loop;
end $$;

drop policy if exists "numeris_manual_portfolio_owner_select" on public.numeris_manual_portfolio;
create policy "numeris_manual_portfolio_owner_select"
on public.numeris_manual_portfolio for select
using (auth.uid() = user_id);

drop policy if exists "numeris_manual_portfolio_owner_insert" on public.numeris_manual_portfolio;
create policy "numeris_manual_portfolio_owner_insert"
on public.numeris_manual_portfolio for insert
with check (auth.uid() = user_id);

drop policy if exists "numeris_manual_portfolio_owner_update" on public.numeris_manual_portfolio;
create policy "numeris_manual_portfolio_owner_update"
on public.numeris_manual_portfolio for update
using (auth.uid() = user_id)
with check (auth.uid() = user_id);

drop policy if exists "numeris_manual_portfolio_owner_delete" on public.numeris_manual_portfolio;
create policy "numeris_manual_portfolio_owner_delete"
on public.numeris_manual_portfolio for delete
using (auth.uid() = user_id);

drop policy if exists "numeris_interactions_owner_read" on public.numeris_interactions;
create policy "numeris_interactions_owner_read"
on public.numeris_interactions for select
using (user_id is null or auth.uid() = user_id);

drop policy if exists "numeris_analysis_owner_read" on public.numeris_analysis_history;
create policy "numeris_analysis_owner_read"
on public.numeris_analysis_history for select
using (auth.uid() = user_id);

drop policy if exists "numeris_backups_owner_read" on public.numeris_user_backups;
create policy "numeris_backups_owner_read"
on public.numeris_user_backups for select
using (auth.uid() = user_id);

-- One-time legacy data copy from AI-generated table names when they exist.
do $$
declare
  legacy text;
  target text;
begin
  for legacy, target in
    select * from (values
      ('quantmind_modules','numeris_modules'),
      ('quantmind_signals','numeris_signals'),
      ('quantmind_simulations','numeris_simulations'),
      ('quantmind_reports','numeris_reports'),
      ('quantmind_event_stream','numeris_event_stream'),
      ('quantmind_brain_traits','numeris_brain_traits'),
      ('quantmind_brain_regions','numeris_brain_regions'),
      ('quantmind_entrance_sequences','numeris_entrance_sequences'),
      ('quantmind_geopolitical_news','numeris_geopolitical_news')
    ) as mappings(legacy, target)
  loop
    if to_regclass('public.' || legacy) is not null then
      execute format('insert into public.%I select * from public.%I on conflict do nothing', target, legacy);
    end if;
  end loop;
end $$;
